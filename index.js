// Não podemos criar variáveis com palavras reservadas da linguagem
// Variáveis precisam ter nomes significativos
// Não começar o nome de uma variável com um número
// Não podem conter espacos, traços ou caracteres especiais
// Variaveis no javascript utilizam camelCase
// Javascript é case-sensitive
// Não é possível redeclarar variáveis com let
// Não utilizar var, preferir por let ao criar variáveis

let nomeCliente; // Declarou a variável
nomeCliente = "João Marcos"; // Inicializando a variável
console.log(nomeCliente);

nomeCliente = "Carlos Junior"; // Redeclarando a variável `nome`
console.log(nomeCliente);


let nomeCompleto = "Lucas Silva";
let nomecompleto = "Yuri Ramos";

console.log(nomeCompleto);
console.log(nomecompleto);